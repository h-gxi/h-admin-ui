<template>
  <div>
    <component v-bind:is="`nm-login-${type}`"></component>
  </div>
</template>
<script>
import { mapState } from 'vuex'
export default {
  computed: {
    ...mapState('app/system', { loginOptions: s => s.loginOptions }),
    type() {
      return this.loginOptions.type || 'default'
    }
  }
}
</script>
